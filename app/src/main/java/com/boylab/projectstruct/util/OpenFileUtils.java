    //android获取一个用于打开Excel文件的intent
    public static Intent getExcelFileIntent(File file, Context context) {
        Intent intent = new Intent("android.intent.action.VIEW");
        intent.addCategory("android.intent.category.DEFAULT");
        intent.addFlags(Intent.FLAG_ACTIVITY_NEW_TASK);
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.N) {
            intent.addFlags(Intent.FLAG_GRANT_READ_URI_PERMISSION);
            intent.addFlags(Intent.FLAG_GRANT_WRITE_URI_PERMISSION);
            String provider_authorities = context.getPackageName() + ".provider";
            context = context.getApplicationContext();
            Uri uri = FileProvider.getUriForFile(context, provider_authorities, file);
            intent.setDataAndType(uri, "application/vnd.ms-excel");
            grantPermissions(context, intent, uri, true);
        } else {
            Uri uri = Uri.fromFile(file);
            intent.setDataAndType(uri, "application/vnd.ms-excel");
        }
        return intent;
    }

    //android获取一个用于分享zip压缩文件的intent
    public static Intent getZipFileShareIntent(File file, Context context) {
        Intent intent = new Intent(Intent.ACTION_SEND);
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.N) {
            intent.addFlags(Intent.FLAG_GRANT_READ_URI_PERMISSION);
            intent.addFlags(Intent.FLAG_GRANT_WRITE_URI_PERMISSION);
            String provider_authorities = context.getPackageName() + ".provider";
            context = context.getApplicationContext();
            Uri uri = FileProvider.getUriForFile(context, provider_authorities, file);
            intent.setDataAndType(uri, "application/x-zip-compressed");
            grantPermissions(context, intent, uri, true);
            intent.putExtra(Intent.EXTRA_STREAM, uri);
        } else {
            Uri uri = Uri.fromFile(file);
            intent.setDataAndType(uri, "application/x-zip-compressed");
            intent.putExtra(Intent.EXTRA_STREAM, uri);
        }
        return intent;
    }

    public static void grantPermissions(Context context, Intent intent, Uri uri, boolean writeAble) {
        int flag = Intent.FLAG_GRANT_READ_URI_PERMISSION;
        if (writeAble) {
            flag |= Intent.FLAG_GRANT_WRITE_URI_PERMISSION;
        }
        intent.addFlags(flag);
        List<ResolveInfo> resInfoList = context.getPackageManager().queryIntentActivities(intent, PackageManager.MATCH_DEFAULT_ONLY);
        for (ResolveInfo resolveInfo : resInfoList) {
            String packageName = resolveInfo.activityInfo.packageName;
            context.grantUriPermission(packageName, uri, flag);
        }
    }

